$('input[type=radio]').on('change',function(){
    const radios = $('input[type=radio]');
    
    let selections = [];
    for (radio of radios){
        if ($(radio).prop('checked')){
            selections.push(radio);
        }
    }

    let key = '身長';
    let sort = 1;
    for (selection of selections){

        option = $(selection).attr('id');
        switch (option) {
            case 'data_height':
            key = '身長';
            break;
            case 'data_weight':
            key = '体重';
            break;
            case 'data_age':
            key = '年齢';
            break;
            case 'data_B':
            key = 'B';
            break;
            case 'data_W':
            key = 'W';
            break;
            case 'data_H':
            key = 'H';
            break;
            case 'sort_1':
            sort = 1;
            break;
            case 'sort_0':
            sort = 0;
            break;
            case 'sort_-1':
            sort = -1;
            break;
        }
    }
    console.log('options', key, sort);
    series = make_data(key, sort);
    drawChart();
});



