function image_overlay(){
    const elem_gs = $('rect');

    for (elem_g of elem_gs){
        const color = $(elem_g).attr('stroke');

        
        const found_idol = idols.find(element => element['カラーコード'] == color);

        let idol = "";
        if (found_idol) idol = found_idol['name'];


        if (idol){
            const img = `<image href="images/${idol}.png" x="${$(elem_g).attr('x')}" y="${$(elem_g).attr('y')}" height="${$(elem_g).attr('height')}" width="${$(elem_g).attr('width')}" preserveAspectRatio="xMidYMin slice"/>`;

            elem_g.insertAdjacentHTML('beforebegin',img);
        }


    
        
    }
}

function make_data(key, sort){
    let series = [];
    for (idol of idols){
        series.push({
            name: idol['名前'],
            data: [parseFloat(idol[key])],
            color: 'rgba(0,0,0,0)',
            borderColor: idol['カラーコード'],
            borderWidth: 2,
        })
    }

    series.sort((a, b) => {
        if (a.data[0] > b.data[0]) return sort;
        if (a.data[0] < b.data[0]) return -1*sort;
        return 0;
    });
    return series;
}

